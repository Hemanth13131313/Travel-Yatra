export const buildTripPlannerPrompt = (userInput: {
  destination?: string;
  startDate?: string;
  endDate?: string;
  budget?: string;
  travellers?: string;
  interests?: string[];
  travelStyle?: string;
  specialRequirements?: string;
}): string => {
  const parts: string[] = ['Please create a comprehensive trip plan based on the following preferences:'];

  if (userInput.destination) parts.push(`Destination: ${userInput.destination}`);
  if (userInput.startDate) parts.push(`Start Date: ${userInput.startDate}`);
  if (userInput.endDate) parts.push(`End Date: ${userInput.endDate}`);
  if (userInput.budget) parts.push(`Budget: ${userInput.budget}`);
  if (userInput.travellers) parts.push(`Travellers: ${userInput.travellers}`);
  if (userInput.interests && userInput.interests.length > 0) parts.push(`Interests: ${userInput.interests.join(', ')}`);
  if (userInput.travelStyle) parts.push(`Travel Style: ${userInput.travelStyle}`);
  if (userInput.specialRequirements) parts.push(`Special Requirements: ${userInput.specialRequirements}`);

  parts.push('\nPlease respond with ONLY a valid JSON object. No markdown, no code fences, no explanatory text.');

  return parts.join('\n');
};
